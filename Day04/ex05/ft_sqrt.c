int	ft_sqrt(int nb)
{
	int			tmp;
	long		sq;

	tmp = nb / 2;
	sq = 0;
	if (nb < 0)
		return (0);
	while (sq * sq <= nb)
	{
		if (sq * sq == nb)
			return (sq);
		++sq;
	}
	return (0);
}
